public class LandV3 {
    public long x,y,z;
    public LandV3(long x,long y,long z){
        this.x=x;
        this.y=y;
        this.z=z;
    }
    public long getX(){
        return x;
    }
    public long getY(){
        return y;
    }
    public long getZ(){
        return z;
    }
}
